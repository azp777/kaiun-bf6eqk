function pickOmamori(p){
  const t=new Date();
  const key=`omamori|${t.getFullYear()}-${t.getMonth()+1}-${t.getDate()}|${p.y}-${p.m}-${p.d}`;
  return OMAMORI[hashStr(key)%OMAMORI.length];
}
function omamoriHTML(p){
  const o=pickOmamori(p);
  return `
    <h3>🌿 今日のこころのお守り</h3>
    <p class="desc"><b>${esc(o.n)}</b> ─ ${esc(o.d)}</p>
    <p class="note">今日の一歩:${esc(o.s)}</p>`;
}
