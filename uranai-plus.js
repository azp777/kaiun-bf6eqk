const TABS_KEY='kaiun-compass-tabs';
const TAB_DEFS=[
 ['suuhi','数秘術'],['houi','吉方位'],['seiza','星占い'],['maya','マヤ暦'],
 ['rokusei','運命星'],['zokusei','神社属性'],['mbti','性格タイプ'],
 ['aishou','💞 相性診断'],['chat','🔮 相談室']
];
function getTabPrefs(){
  try{return JSON.parse(localStorage.getItem(TABS_KEY))||{};}catch(e){return {};}
}
function switchTab(key){
  document.querySelectorAll('#tabbar .tab').forEach(t=>t.classList.toggle('active',t.dataset.tab===key));
  document.querySelectorAll('.tabpane').forEach(s=>s.classList.toggle('hidden',s.id!=='tab-'+key));
  if(key==='chat'&&typeof chatWelcome==='function')chatWelcome();
  window.scrollTo(0,0);
}
function applyTabPrefs(){
  const prefs=getTabPrefs();
  let activeHidden=false;
  document.querySelectorAll('#tabbar .tab').forEach(t=>{
    const k=t.dataset.tab;
    const on=k==='sougou'||prefs[k]!==false;
    t.classList.toggle('hidden',!on);
    if(!on&&t.classList.contains('active'))activeHidden=true;
  });
  if(activeHidden)switchTab('sougou');
}
function openTabSettings(){
  if(document.getElementById('tabset-overlay'))return;
  const prefs=getTabPrefs();
  const ov=document.createElement('div');
  ov.className='tabset-overlay';
  ov.id='tabset-overlay';
  const rows=[['sougou','✦ 総合',true]].concat(TAB_DEFS.map(d=>[d[0],d[1],false]))
   .map(([k,label,lock])=>`<div class="tabset-row"><span>${label}${lock?' <span class="tabset-lock-note">(常に表示)</span>':''}</span><button type="button" class="tgl ${lock||prefs[k]!==false?'on':''}${lock?' lock':''}" data-key="${k}" ${lock?'disabled':''} aria-label="${label}の表示切替"></button></div>`).join('');
  ov.innerHTML=`<div class="tabset-modal">
    <h2>⚙ タブの表示設定</h2>
    <p class="section-lead">よく使う占いだけを表示できます。設定はこの端末にのみ保存されます。</p>
    ${rows}
    <button class="btn" style="margin-top:18px" onclick="closeTabSettings()">閉じる</button>
  </div>`;
  ov.addEventListener('click',e=>{
    if(e.target===ov){closeTabSettings();return;}
    const btn=e.target.closest('.tgl');
    if(!btn||btn.classList.contains('lock'))return;
    const key=btn.dataset.key;
    const p2=getTabPrefs();
    const cur=p2[key]!==false;
    p2[key]=!cur;
    localStorage.setItem(TABS_KEY,JSON.stringify(p2));
    btn.classList.toggle('on',!cur);
    applyTabPrefs();
  });
  document.body.appendChild(ov);
}
function closeTabSettings(){
  const ov=document.getElementById('tabset-overlay');
  if(ov)ov.remove();
}
/* 総合タブの mini カード → 各タブへジャンプ(委譲リスナー) */
document.getElementById('tab-sougou').addEventListener('click',e=>{
  const mini=e.target.closest('.mini[data-jump]');
  if(!mini)return;
  const key=mini.dataset.jump;
  if(key!=='sougou'&&getTabPrefs()[key]===false)return; // 非表示設定のタブへは飛ばない
  switchTab(key);
});

